        private void VerifyBookingButtonActionPerformed(java.awt.event.ActionEvent evt) {
        if(!BookingIDField.getText().equals("")){
            try{
            //credentials
            String url="jdbc:derby://localhost:1527/BookingSystemDB/";
            String username ="operator";
            String password = "password";
            Connection con = DriverManager.getConnection(url, username, password);
            String checkID = "SELECT * from BOOKING WHERE BOOKINGREF = " + BookingIDField.getText()+ "";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(checkID); 

            if(rs.next()) {//booking exists
                JOptionPane.showMessageDialog(null, "Booking exists.");
                EditBookingButton.setEnabled(true);
                DeleteBookingButton.setEnabled(true);
            }
            else{//booking does not exist
                JOptionPane.showMessageDialog(null, "Booking does not exist.");
                EditBookingButton.setEnabled(false);
                DeleteBookingButton.setEnabled(false);
            }
            
            jLabel11.setText(rs.getString("BOOKINGDATE"));
            jLabel12.setText(rs.getString("BOOKINGDEPARTURE"));
            jLabel13.setText(rs.getString("BOOKINGDESTINATION"));
            jLabel14.setText("£"+rs.getString("BOOKINGCOST"));
            
            //display booking information
            BookingInfoFrame.setVisible(true);
            
            }
            catch(SQLException ex){
               JOptionPane.showMessageDialog(null, ex.toString());
            }
            }
        else{
            JOptionPane.showMessageDialog(null, "Booking ID cannot be empty.");
        }
    }  
