    private void EditBookingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBookingButtonActionPerformed
        EditTripFrame.setVisible(true);
        try{
                //credentials
                String url="jdbc:derby://localhost:1527/BookingSystemDB/";
                String username ="operator";
                String password = "password";
                Connection con = DriverManager.getConnection(url, username, password);
                Statement stmt = con.createStatement();   
                String Query = "SELECT * from BOOKING WHERE BOOKINGREF = " + BookingIDField.getText()+ "";
                stmt.execute(Query);
                ResultSet temp = stmt.getResultSet();
                temp.next();
                DestinationBox1.setSelectedItem(temp.getString("BOOKINGDESTINATION"));
                DepartureBox1.setSelectedItem(temp.getString("BOOKINGDEPARTURE"));;
                String bookingdate = new String(temp.getString("BOOKINGDATE"));
                String Year = Character.toString((bookingdate.charAt(0)))+Character.toString((bookingdate.charAt(1)))+Character.toString((bookingdate.charAt(2)))+Character.toString((bookingdate.charAt(3)));
                String Month = Character.toString((bookingdate.charAt(5)))+ Character.toString((bookingdate.charAt(6)));
                String Day = Character.toString((bookingdate.charAt(8)))+ Character.toString((bookingdate.charAt(9)));
                YearBox1.setSelectedItem(Year);
                MonthBox1.setSelectedItem(Month);
                DayBox1.setSelectedItem(Day);
                JOptionPane.showMessageDialog(null, Day);
                if((temp.getInt("BOOKINGCOST")>21)){
                    NonMemberBox1.setSelected(true);
                    MemberBox1.setSelected(false);
                }
                else{
                    MemberBox1.setSelected(true);
                    NonMemberBox1.setSelected(false);
                }
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex.toString());
            }
    }//GEN-LAST:event_EditBookingButtonActionPerformed

